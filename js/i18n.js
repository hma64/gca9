export function getLang() {
  const v = localStorage.getItem(LANG_KEY);
  return v === "ar" ? "ar" : "fr";
}

export function setLang(lang) {
  localStorage.setItem(LANG_KEY, lang === "ar" ? "ar" : "fr");
}

export function t(key) {
  const lang = getLang();
  return i18n[lang][key] ?? i18n.fr[key] ?? key;
}
